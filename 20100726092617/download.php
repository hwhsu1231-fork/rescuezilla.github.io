    <a class="section_anchor" id="wb_2" name="wb_2"></a><h1><img align="right" width="128" height="128" src="http://redobackup.org/media/icons/cd.png" style="margin: 0px 0px 10px 10px;" alt="" />Get Redo Backup</h1>
<h2>1. Download the latest disc image</h2>
<p>Download the latest version of Redo Backup from <a rel="nofollow" href="https://sourceforge.net/projects/redobackup/">here</a>.</p>
<p><strong>WE&nbsp;NEED&nbsp;FEEDBACK. Please consider writing a review of this new version on SourceForge, especially if you like it and it works for you!</strong>&nbsp;If you find a problem, please go <a href="/bugs/" rel="nofollow">here</a> to tell us how we can improve Redo Backup.</p>
<h2>2. Burn the image to a CD</h2>
<p>You'll need to burn the ISO&nbsp;disc image using a CD&nbsp;burning program (such as <a rel="nofollow" target="_blank" href="http://infrarecorder.org/">InfraRecorder</a> for Windows).</p>
<h2>3. Put the CD in and reboot</h2>
<p>Place the new CD in your CD-ROM&nbsp;drive and reboot the machine. To boot from the CD-ROM drive, you may need to press a key while your computer is starting up, (such as the F8 key).</p>
<h3>Installing to a USB Stick</h3>
<p>If you would like to install Redo Backup to a USB stick, you will need to follow the instructions above to create a CD&nbsp;and boot from it. Once the system starts, connect your USB&nbsp;stick into the machine and click the &quot;Install to USB&nbsp;Drive&quot; option from the main menu. After the installation is complete, you can use the USB&nbsp;stick to boot on a netbook or other device that is not equipped with a CD-ROM drive.</p>  </div>
