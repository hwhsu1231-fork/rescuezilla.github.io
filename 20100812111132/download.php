    <a class="section_anchor" id="wb_2" name="wb_2"></a><h1><img align="right" width="128" height="128" alt="" style="margin: 0px 0px 10px 10px;" src="http://redobackup.org/media/icons/cd.png" />Get Redo Backup</h1>
<p><strong>We need feedback! If you like it, please consider giving the project a &quot;thumbs up&quot; on SourceForge.</strong> If you find a problem, please go <a rel="nofollow" href="/bugs/">here</a> to tell us how we can improve Redo Backup. It is a <a rel="nofollow" target="_blank" href="http://en.wikipedia.org/wiki/Live_cd">live CD</a>,  so you cannot run the program from within Windows or any other operating  system; you need to follow the steps below exactly as they are presented in order to use Redo Backup.<strong><br />
</strong></p>
<h2>1. Download the latest live CD image</h2>
<p>Download the latest version of the Redo Backup live CD&nbsp;from <a href="https://sourceforge.net/projects/redobackup/" rel="nofollow">here</a>.<strong><br />
</strong></p>
<h2>2. Burn the image to a CD</h2>
<p>You'll need to burn the ISO&nbsp;disc image using a CD&nbsp;burning program such as <a href="http://infrarecorder.org/" target="_blank" rel="nofollow">InfraRecorder</a> for Windows.</p>
<h2>3. Put the CD in and reboot</h2>
<p>Redo Backup does not run from inside Windows or any other operating system <strong>because it is one</strong><strong>!</strong> You must restart your computer with the CD&nbsp;in the drive in order to use Redo Backup.&nbsp;Place the new CD in your CD-ROM&nbsp;drive and reboot the machine. To boot from the CD-ROM drive, you may need to press a key such as <strong>F8</strong> or <strong>F12 </strong>while your computer is starting up.</p>
<p>After the CD&nbsp;boots, you will have a mini operating system loaded into memory which will launch Redo Backup. It will not install anything to your hard drive. You decide when and where you want to save any backup images, if you decide to create one.</p>
<h3>Installing to a USB Stick</h3>
<p>There are two ways to create a bootable USB&nbsp;stick with Redo Backup on it:&nbsp;1)&nbsp;Burn a CD, boot from it, and then use the &quot;Install to USB&nbsp;Drive&quot; option from the main menu; or 2) use a program such as Unetbootin to put the CD&nbsp;image directly on a USB&nbsp;stick&nbsp;(without burning a CD).</p>
<h3>Using Unetbootin</h3>
<p>Burning a CD&nbsp;is the easiest and most straightforward way to put Redo on a USB&nbsp;stick, but sometimes (as with a netbook)&nbsp;you may not have access to a CD writer. Advanced users may then wish to use Unetbootin. <strong>If you use an old version of Unetbootin it will not work.</strong> You must have <a href="http://sourceforge.net/projects/unetbootin/" target="_blank" rel="nofollow">the latest version of Unetbootin</a>.</p>
<p><strong>Using Unetbootin is not recommended because it prevents the Redo Backup splash screen, help files, boot menu messages, and other features from being displayed or used.</strong> Please let the maintainers of Unetbootin know that you would like these issues to be fixed, and Redo Backup added to the list of built-in images. Just go to <a href="http://bugs.launchpad.net/unetbootin" target="_blank" rel="nofollow">http://bugs.launchpad.net/unetbootin</a> and open a bug report to do so.</p>  </div>
