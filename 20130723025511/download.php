<h2><strong>Getting Started</strong></h2>
<div class="faq rounded">
<h3>1. Download the latest live CD image</h3>
<p>Download the latest version of the Redo Backup live CD&nbsp;from <a rel="nofollow" href="https://sourceforge.net/projects/redobackup/">here</a>.<strong><br/>
</strong></p>
<h3>2. Burn the image to a CD</h3>
<p>You'll need to burn the ISO&nbsp;disc image using a CD&nbsp;burning program such as <a rel="nofollow" target="_blank" href="http://infrarecorder.org/">InfraRecorder</a> for Windows.</p>
<h3>3. Put the CD in and reboot</h3>
<p>Redo Backup does not run from inside Windows or any other operating system <strong>because it is one</strong><strong>!</strong> You must restart your computer with the CD&nbsp;in the drive in order to use Redo Backup.&nbsp;Place the new CD in your CD-ROM&nbsp;drive and reboot the machine. To boot from the CD-ROM drive, you may need to press a key such as <strong>F8</strong> or <strong>F12 </strong>while your computer is starting up.</p>
</div>
<p>After the CD&nbsp;boots, you will have a mini operating system loaded into memory which will launch Redo Backup. It will not install anything to your hard drive. You decide when and where you want to save any backup images, if you choose to do so.</p>
<h2>System Requirements</h2>
<ul>
    <li>PC&nbsp;(Intel/AMD compatible)</li>
    <li>512MB&nbsp;RAM</li>
</ul>
<h2>Installing to a USB&nbsp;Stick&nbsp;(Optional)</h2>
<p>There are two ways to create a bootable USB&nbsp;stick with Redo Backup on it:</p>
<h3>1. Installing from CD to USB</h3>
<p>Burn a CD, boot from it, and then use the &quot;Install to USB&nbsp;Drive&quot; tool from the start menu. This is the easiest and most straightforward way to put Redo on a USB&nbsp;stick.</p>
<h3>2. Installing Directly to USB&nbsp;with Unetbootin</h3>
<p>Sometimes, as with a netbook, you may not have easy access to a CD-ROM&nbsp;drive. Advanced users may then wish to use Unetbootin to install the ISO&nbsp;image directly to a USB&nbsp;stick, bypassing the step of burning a CD.&nbsp;<strong>If you use an older version of Unetbootin, it will not work.</strong> You must have <a rel="nofollow" target="_blank" href="http://sourceforge.net/projects/unetbootin/">the latest version of Unetbootin</a> downloaded directly from their website.</p>
<p><strong>Using Unetbootin is not recommended</strong> because it prevents the Redo Backup splash screen, help files, boot menu messages, and other features from being displayed or used. Please let the maintainers of Unetbootin know that you would like these issues to be fixed, and Redo Backup added to the list of built-in images! Just go to <a rel="nofollow" target="_blank" href="http://bugs.launchpad.net/unetbootin">http://bugs.launchpad.net/unetbootin</a> and open a bug report to do so.</p>  </div>
